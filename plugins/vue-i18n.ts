import { createI18n } from 'vue-i18n'
import type { NuxtApp } from 'nuxt3'

export default function (nuxt: NuxtApp) {
  const { app } = nuxt
  const i18n = createI18n({
    legacy: false,
    locale: 'en',
    messages: {
      en: {
        hello: 'Hello {name}!',
        language: 'Language'
      },
      ja: {
	      hello: 'こんにちは {name}！',
        language: '言語'
      }
    }
  })

  app.use(i18n)
}